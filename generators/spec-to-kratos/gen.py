#!/usr/bin/env python3
"""
spec-to-kratos v0.1
从 L5c (data) + L5b (service) spec 生成 go-kratos domain layer

用法:
  python3 gen.py <spec_dir> <output_dir> [--pkg domain|service|both]

生成产物:
  internal/domain/canvas/   — domain entities (pb.go)
  api/canvas/service/v1/    — service proto + client
  internal/service/canvas/ — service implementation stubs
"""

import sys
import os
import yaml
import re
import shutil
from pathlib import Path
from datetime import date

_spec_arg = Path(sys.argv[1]) if len(sys.argv) > 1 else Path("specs")
# Accept either a specs/ directory or a specs/feature/xxx/xxx_data.yaml file
# Normalize: always resolve to the specs/ root directory
if _spec_arg.is_file():
    # specs/feature/ai-runtime/ai_runtime_data.yaml → find specs/ root
    _cur = _spec_arg
    while _cur.name != "specs" and _cur.parent != _cur:
        _cur = _cur.parent
    SPEC_DIR = _cur if _cur.name == "specs" else _spec_arg
elif _spec_arg.is_dir():
    SPEC_DIR = _spec_arg
else:
    SPEC_DIR = _spec_arg  # fallback
OUT_DIR = Path(sys.argv[2]) if len(sys.argv) > 2 else Path("backend")
PKG_MODE = "both"
for arg in sys.argv[3:]:
    if "--pkg=" in arg:
        PKG_MODE = arg.split("=")[1]

TODAY = date.today().isoformat()
GEN_COMMENT = f"""// Code generated by spec-to-kratos v0.1 — {TODAY}
// DO NOT EDIT. Regenerate with: python3 generators/spec-to-kratos/gen.py
"""

# ── YAML 加载 ────────────────────────────────────────────────
def load_yaml(path: Path) -> dict:
    with open(path, encoding="utf-8") as f:
        return yaml.safe_load(f)

def snake_to_camel(s: str) -> str:
    """snake_case → CamelCase"""
    return "".join(p.capitalize() for p in s.split("_"))

def go_type(yaml_type: str) -> str:
    mapping = {
        "string": "string",
        "number": "float64",
        "integer": "int32",
        "boolean": "bool",
        "datetime": "time.Time",
        "json": "[]byte",
    }
    return mapping.get(yaml_type, yaml_type)

# ── domain 实体生成 ──────────────────────────────────────────
def gen_domain_entity(entity: dict) -> str:
    name = entity["name"]
    pkg = snake_to_camel(name).lower()

    fields = []
    for f in entity.get("fields", []):
        fname = f["name"]
        ftype = go_type(f["type"])
        tag = f'`json:"{fname}"`'
        comment = f.get("description", "")
        if comment:
            comment = " // " + comment
        fields.append(f"    {fname} {ftype} {tag}{comment}")

    field_str = "\n".join(fields)

    return f"""package canvas

{ GEN_COMMENT }

type {name} struct {{
{field_str}
}}

func ({name[0]}) Descriptor() ([]byte, []int) {{
    return []byte(nil), nil
}}
"""

def gen_domain_errors(entity: dict, feature: str = "canvas") -> str:
    name = entity["name"]
    lname = name.lower()
    feat_clean = feature.replace("-", "")
    return f"""
var (
    Err{name}NotFound = errors.New("{feat_clean}: {lname} not found")
    Err{name}Invalid  = errors.New("{feat_clean}: invalid {lname}")
)
"""

# ── proto 生成 ───────────────────────────────────────────────
def gen_proto(service: dict, feature: str = "canvas") -> str:
    svc_name = service.get("name", "")
    feat_clean = feature.replace("-", "")
    methods = []
    for m in service.get("methods", []):
        mname = m["name"]
        req_fields = []
        for f in m.get("request", {}).get("body", []):
            if isinstance(f, dict) and "field" in f:
                req_fields.append(f"  {f['field']}: {go_type(f['type'])}")
        for f in m.get("request", {}).get("params", []):
            if isinstance(f, dict) and "field" in f:
                req_fields.append(f"  {f['field']}: {go_type(f['type'])}")
        for f in m.get("request", {}).get("query", []):
            if isinstance(f, dict) and "field" in f:
                req_fields.append(f"  {f['field']}: {go_type(f['type'])}")
        resp_fields = []
        for f in m.get("response", {}).get("body", []):
            if isinstance(f, dict) and "field" in f:
                resp_fields.append(f"  {f['field']}: {go_type(f['type'])}")

        req_struct = f"message {mname}Request {{" + ("\n" + "\n".join(req_fields) + "\n}" if req_fields else "}")
        resp_struct = f"message {mname}Response {{" + ("\n" + "\n".join(resp_fields) + "\n}" if resp_fields else "}")

        method_rpc = f"  rpc {mname}({mname}Request) returns ({mname}Response);"
        methods.extend([req_struct, resp_struct, method_rpc, ""])

    methods_str = "\n".join(methods).rstrip()
    return f"""syntax = "proto3";

package {feat_clean}.service.v1;

option go_package = "vibex/api/{feat_clean}/service/v1;v1";

{methods_str}
"""

# ── service interface 生成 ───────────────────────────────────
def gen_service_interface(service: dict, feature: str = "canvas") -> str:
    svc_name = service.get("name", "")
    methods = []
    for m in service.get("methods", []):
        mname = m["name"]
        req = m.get("request", {})
        resp = m.get("response", {})

        in_params = []
        for f in req.get("params", []):
            if isinstance(f, dict) and "field" in f:
                in_params.append(f"{f['field']} {go_type(f['type'])}")
        for f in req.get("body", []):
            if isinstance(f, dict) and "field" in f:
                in_params.append(f"{f['field']} {go_type(f['type'])}")
        params_str = ", ".join(in_params)
        ret_types = []
        for f in resp.get("body", []):
            if isinstance(f, dict) and "field" in f:
                ret_types.append(go_type(f["type"]))
        ret_types.append("error")

        params_str = ", ".join(in_params)
        ret_str = ", ".join(ret_types)
        comment = f"    // {m['http_method']} {req.get('params', [{}])[0].get('endpoint', mname) if req.get('params') else m.get('endpoint', '')}"
        methods.append(f"{comment}\n    {mname}(ctx context.Context, {params_str}) ({ret_str})")

    methods_str = "\n".join(methods)
    return f"""package service

{ GEN_COMMENT }

import (
    "context"
    "time"
)

type {svc_name} interface {{
{methods_str}
}}
"""

# ── service stub 生成 ────────────────────────────────────────
def gen_service_stub(service: dict, feature: str = "canvas") -> str:
    svc_name = service.get("name", "")
    methods_impl = []
    for m in service.get("methods", []):
        mname = m["name"]
        req = m.get("request", {})
        in_params = []
        for f in req.get("params", []):
            if isinstance(f, dict) and "field" in f:
                in_params.append(f"{f['field']} {go_type(f['type'])}")
        for f in req.get("body", []):
            if isinstance(f, dict) and "field" in f:
                in_params.append(f"{f['field']} {go_type(f['type'])}")
        params_str = ", ".join(in_params)
        ret_types = []
        for f in m.get("response", {}).get("body", []):
            if isinstance(f, dict) and "field" in f:
                ret_types.append(go_type(f["type"]))
        ret_types.append("error")
        ret_str = ", ".join(ret_types)
        zero_vals = []
        for f in m.get("response", {}).get("body", []):
            if isinstance(f, dict) and "field" in f:
                t = go_type(f["type"])
                if t == "string":
                    zero_vals.append('""')
                elif t == "int32":
                    zero_vals.append("0")
                elif t == "float64":
                    zero_vals.append("0.0")
                elif t == "bool":
                    zero_vals.append("false")
                elif t == "time.Time":
                    zero_vals.append("time.Time{}")
                elif t == "[]byte":
                    zero_vals.append("nil")
                else:
                    zero_vals.append("nil")
        zero_vals.append("nil")
        methods_impl.append(f"""
func (s *{svc_name}Server) {mname}(ctx context.Context, {params_str}) ({ret_str}) {{
    // TODO: implement {mname}
    return {", ".join(zero_vals)}
}}
""")

    methods_str = "".join(methods_impl)
    return f"""package service

{ GEN_COMMENT }

import (
    "context"
    "time"
)

type {svc_name}Server struct {{}}

func New{svc_name}Server() *{svc_name}Server {{
    return &{svc_name}Server{{}}
}}

{methods_str}
"""

# ── data.yaml → domain files ────────────────────────────────
def process_data_spec(data_yaml: Path, out_dir: Path, feature_name: str):
    data = load_yaml(data_yaml)
    domain_dir = out_dir / "internal" / "domain" / feature_name.replace("-", "")
    domain_dir.mkdir(parents=True, exist_ok=True)

    errors_out = out_dir / "internal" / "domain" / "errors.go"
    all_errors = []

    for entity in data.get("content", {}).get("entities", []):
        name = entity["name"]
        fpath = domain_dir / f"{name.lower()}.go"
        fpath.write_text(gen_domain_entity(entity))
        print(f"  → {fpath}")
        all_errors.append(gen_domain_errors(entity, feature_name))

    # Write errors
    err_content = f"""package {feature_name.replace("-", "")}

import "errors"

{ GEN_COMMENT }
"""
    for e in all_errors:
        err_content += e.lstrip()
    errors_out.write_text(err_content)
    print(f"  → {errors_out}")

# ── service.yaml → proto + interface + stub ────────────────
def process_service_spec(service_yaml: Path, out_dir: Path, feature_name: str):
    data = load_yaml(service_yaml)
    feat_clean = feature_name.replace("-", "")
    proto_dir = out_dir / "api" / feat_clean / "service" / "v1"
    svc_dir = out_dir / "internal" / "service" / feat_clean
    proto_dir.mkdir(parents=True, exist_ok=True)
    svc_dir.mkdir(parents=True, exist_ok=True)

    for api in data.get("content", {}).get("apis", []):
        svc_name = api.get("name", "")

        # proto
        proto_path = proto_dir / f"{svc_name.lower()}.proto"
        proto_path.write_text(gen_proto(api, feature_name))
        print(f"  → {proto_path}")

        # interface
        iface_path = svc_dir / f"{svc_name.lower()}_interface.go"
        iface_path.write_text(gen_service_interface(api, feature_name))
        print(f"  → {iface_path}")

        # stub
        stub_path = svc_dir / f"{svc_name.lower()}_stub.go"
        stub_path.write_text(gen_service_stub(api, feature_name))
        print(f"  → {stub_path}")

# ── main ────────────────────────────────────────────────────
def main():
    # Resolve spec_root: find the directory containing 'feature' subdirectory
    spec_root = SPEC_DIR
    while spec_root.name != "feature" and spec_root.parent != spec_root:
        candidate = spec_root / "feature"
        if candidate.is_dir():
            break
        spec_root = spec_root.parent

    print(f"spec-to-kratos v0.1")
    print(f"  spec_dir : {spec_root}")
    print(f"  output_dir: {OUT_DIR}")
    print(f"  mode     : {PKG_MODE}")

    features = [d.name for d in (spec_root / "feature").iterdir() if d.is_dir()]

    for feature in features:
        feature_path = spec_root / "feature" / feature
        data_yaml = feature_path / f"{feature}_data.yaml"
        service_yaml = feature_path / f"{feature}_service.yaml"

        print(f"\n[{feature}]")

        if data_yaml.exists():
            process_data_spec(data_yaml, OUT_DIR, feature)
        else:
            print(f"  (no data.yaml for {feature}, skipping domain generation)")

        if service_yaml.exists():
            process_service_spec(service_yaml, OUT_DIR, feature)
        else:
            print(f"  (no service.yaml for {feature}, skipping service generation)")

    print(f"\n✅ Done — output: {OUT_DIR}")

if __name__ == "__main__":
    main()
