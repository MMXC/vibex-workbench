#!/usr/bin/env python3
"""
scaffolder.py — VibeX Workbench Scaffold Generator

在空/半空仓库生成最小可跑骨架。

用法:
    python3 scaffolder.py /path/to/workspace [--template default]
    python3 scaffolder.py /path/to/workspace --json

生成产物：
    specs/L1-goal/ENTRY.yaml
    generators/gen.py
    generators/validate_specs.py
    Makefile
    README.md
"""

import json
import os
import shutil
import sys
from datetime import datetime
from pathlib import Path

# 模板根目录：相对于当前脚本的 spec-templates/
SCRIPT_DIR = Path(__file__).parent.resolve()
TEMPLATE_ROOT = SCRIPT_DIR.parent / "spec-templates"

# 占位符替换表
PLACEHOLDERS = {
    "{project-name}": "my-project",
    "{owner}": "user",
    "{YYYY-MM-DD}": datetime.now().strftime("%Y-%m-%d"),
    "{YYYY-MM-DDTHH:MM:SS}": datetime.now().strftime("%Y-%m-%dT%H:%M:%S"),
}

# Makefile 模板内容
MAKEFILE_TEMPLATE = """.PHONY: lint-specs generate validate help

help:
\t@echo "VibeX Workbench — available targets:"
\t@echo "  make lint-specs  — validate all specs"
\t@echo "  make generate    — run spec-to-code generators"
\t@echo "  make validate    — alias for lint-specs"

lint-specs:
\t@echo "[lint-specs] Checking specs..."
\t@cd generators && python3 validate_specs.py

generate:
\t@echo "[generate] Running generators..."
\t@cd generators && python3 gen.py

validate: lint-specs
"""

# generators/gen.py 内容（内联桩）
GEN_PY_TEMPLATE = '''#!/usr/bin/env python3
"""
generators/gen.py — VibeX Spec-to-Code Generator Entry Point

This file is generated by VibeX Workbench.
Add your spec-to-code generation logic here.
"""
import sys
import os
from pathlib import Path

PROJECT_ROOT = Path(__file__).parent.parent
sys.path.insert(0, str(PROJECT_ROOT))

def main():
    print("[gen] Generator entry — no rules defined yet.")
    print("[gen] Add your spec parsing and code generation logic here.")
    print("[gen] Run 'make generate' to execute.")

if __name__ == "__main__":
    main()
'''


def substitute(content: str, project_name: str, owner: str) -> str:
    """替换模板占位符"""
    replacements = {
        "{project-name}": project_name,
        "{owner}": owner,
        "{YYYY-MM-DD}": datetime.now().strftime("%Y-%m-%d"),
        "{YYYY-MM-DDTHH:MM:SS}": datetime.now().strftime("%Y-%m-%dT%H:%M:%S"),
    }
    result = content
    for placeholder, value in replacements.items():
        result = result.replace(placeholder, value)
    return result


def _inline_l1_spec(project_name: str, owner: str) -> str:
    """Generate inline L1 spec content when template file is missing."""
    now = datetime.now().strftime("%Y-%m-%d")
    now_ts = datetime.now().strftime("%Y-%m-%dT%H:%M:%S")
    return f"""---
spec:
  version: "0.1"
  level: "1_project_goal"
  name: "{project_name}-goal"
  parent: null
  status: "proposal"
  owner: "{owner}"
  created: "{now}"
  updated: "{now}"

meta:
  type: "project-goal"
  owner: "{owner}"
  created: "{now}"
  updated: "{now}"
  tags: []

lifecycle:
  current: "proposal"
  history:
    - status: "proposal"
      at: "{now_ts}"
      by: "{owner}"
      trigger: "user:manual"
      note: "项目目标初始化"

io_contract:
  input: "用户描述的产品意图（自然语言）"
  output: "L1 goal spec（使命、约束、价值分层）"
  boundary: "只回答做什么/不做什么/成功什么样，不写技术实现"
  behavior: |
    1. 接收用户意图
    2. 写成 L1 goal spec
    3. 确认 spec 完整
    4. 进入 L2 skeleton 阶段

content:
  mission: "待填写：产品的核心使命是什么"
  target_users: "待填写：目标用户是谁"
  constraints: []
  product_value_layers:
    kernel: "内核价值"
    proof: "证明路径"
    experience: "用户体验"
"""


def scaffold(workspace_root: str, template_variant: str = "default",
             project_name: str = "my-project", owner: str = "user",
             mode: str = "full") -> dict:
    """
    在指定 workspace 根目录生成最小可跑骨架。

    Args:
        workspace_root: 目标工作区根目录
        template_variant: "default" | "agent-assisted"（目前统一用 default）
        project_name: 项目名称（用于 L1 spec 文件名）
        owner: 负责人（用于 L1 spec 元数据）
        mode: "full" | "partial"
            full:   在空目录创建所有骨架文件
            partial: 补全缺失文件，已存在的文件（包括 specs/）不覆盖

    Returns:
        {"ok": true, "state": "empty|partial|ready", "written_files": [...], "skipped_files": [...]}
    """
    root = Path(workspace_root).resolve()
    written_files = []
    skipped_files = []
    errors = []

    # 前置：workspace_root 必须是目录
    if not root.is_dir():
        return {"ok": False, "error": f"目录不存在: {workspace_root}"}

    # partial mode: specs/ 目录存在时标记为已跳过，不覆盖
    specs_dir = root / "specs"
    if mode == "partial" and specs_dir.exists():
        skipped_files.append("specs/")

    try:
        # 创建目录（partial mode 下若 specs/ 已存在则不重建）
        if mode == "partial" and specs_dir.exists():
            # 只创建 generators/，不碰 specs/
            gen_dir = root / "generators"
            gen_dir.mkdir(parents=True, exist_ok=True)
        else:
            for d in ["specs/L1-goal", "generators"]:
                p = root / d
                p.mkdir(parents=True, exist_ok=True)

        # 复制 L1 入口模板
        dst_entry = root / "specs" / "L1-goal" / "ENTRY.yaml"
        if mode == "partial" and dst_entry.exists():
            skipped_files.append("specs/L1-goal/ENTRY.yaml")
        else:
            src_entry = TEMPLATE_ROOT / "L1-goal" / "entry.yaml"
            if src_entry.exists():
                content = src_entry.read_text(encoding="utf-8")
                content = substitute(content, project_name, owner)
                dst_entry.write_text(content, encoding="utf-8")
                written_files.append(str(dst_entry.relative_to(root)))
            else:
                dst_entry.write_text(_inline_l1_spec(project_name, owner), encoding="utf-8")
                written_files.append(str(dst_entry.relative_to(root)))

        # 生成 generators/gen.py（写桩，不覆盖已有）
        gen_py = root / "generators" / "gen.py"
        if not gen_py.exists():
            gen_py.write_text(GEN_PY_TEMPLATE, encoding="utf-8")
            written_files.append("generators/gen.py")
        else:
            skipped_files.append("generators/gen.py")

        # 复制 validate_specs.py（不覆盖已有）
        src_validate = SCRIPT_DIR / "validate_specs.py"
        dst_validate = root / "generators" / "validate_specs.py"
        if not dst_validate.exists():
            if src_validate.exists():
                shutil.copy2(src_validate, dst_validate)
                written_files.append("generators/validate_specs.py")
        else:
            skipped_files.append("generators/validate_specs.py")

        # 生成 Makefile（不覆盖已有）
        mk = root / "Makefile"
        if not mk.exists():
            mk.write_text(MAKEFILE_TEMPLATE, encoding="utf-8")
            written_files.append("Makefile")
        else:
            skipped_files.append("Makefile")

        # 生成 README.md（不覆盖已有）
        readme = root / "README.md"
        if not readme.exists():
            readme.write_text(
                f"# {root.name}\n\nGenerated by VibeX Workbench.\n",
                encoding="utf-8"
            )
            written_files.append("README.md")
        else:
            skipped_files.append("README.md")

    except Exception as e:
        errors.append(str(e))

    # 计算最终状态
    has_specs = (root / "specs").exists()
    has_gen_py = (root / "generators" / "gen.py").exists()
    has_makefile = (root / "Makefile").exists()
    if has_specs and has_gen_py and has_makefile:
        state = "ready"
    elif has_specs or has_gen_py or has_makefile:
        state = "partial"
    else:
        state = "empty"

    return {
        "ok": True,
        "state": state,
        "written_files": written_files,
        "skipped_files": skipped_files,
        "errors": errors,
        "workspaceRoot": str(root),
    }


def main():
    as_json = "--json" in sys.argv

    if len(sys.argv) < 2 or "--help" in sys.argv:
        msg = "Usage: scaffolder.py <workspace_root> [--template default] [--mode full|partial] [--json]"
        if as_json:
            print(json.dumps({"ok": False, "error": msg}))
        else:
            print(msg, file=sys.stderr)
        sys.exit(0 if "--help" in sys.argv else 1)

    workspace = sys.argv[1]
    template = "default"
    mode = "full"
    if "--template" in sys.argv:
        idx = sys.argv.index("--template")
        if idx + 1 < len(sys.argv):
            template = sys.argv[idx + 1]
    if "--mode" in sys.argv:
        idx = sys.argv.index("--mode")
        if idx + 1 < len(sys.argv):
            mode = sys.argv[idx + 1]

    # 从环境变量或默认值获取 project_name / owner
    project_name = os.environ.get("VIBEX_PROJECT_NAME", Path(workspace).name)
    owner = os.environ.get("VIBEX_OWNER", "user")

    result = scaffold(workspace, template, project_name, owner, mode=mode)

    if as_json or not sys.stdout.isatty():
        print(json.dumps(result, ensure_ascii=False, indent=2))
    else:
        if result["ok"]:
            print(f"✅ 脚手架生成完成，共 {len(result['created'])} 项：")
            for f in result["created"]:
                print(f"  + {f}")
            if result["errors"]:
                print(f"  ⚠️  错误: {result['errors']}")
        else:
            print(f"❌ 失败: {result.get('error', 'unknown')}")

    sys.exit(0 if result["ok"] else 1)


if __name__ == "__main__":
    main()
