#!/usr/bin/env python3
"""
spec-to-prisma v0.1
从 L5c (data) spec 生成 Prisma schema

用法:
  python3 gen.py <spec_dir> <output_dir>

生成产物:
  prisma/schema.prisma  — 完整 Prisma schema
"""

import sys
import yaml
from pathlib import Path
from datetime import date

_spec_arg = Path(sys.argv[1]) if len(sys.argv) > 1 else Path("specs")
if _spec_arg.is_file():
    _cur = _spec_arg
    while _cur.name != "specs" and _cur.parent != _cur:
        _cur = _cur.parent
    SPEC_DIR = _cur if _cur.name == "specs" else _spec_arg
elif _spec_arg.is_dir():
    SPEC_DIR = _spec_arg
else:
    SPEC_DIR = _spec_arg
OUT_DIR = Path(sys.argv[2]) if len(sys.argv) > 2 else Path("prisma")

TODAY = date.today().isoformat()

# ── YAML 加载 ────────────────────────────────────────────────
def load_yaml(path: Path) -> dict:
    with open(path, encoding="utf-8") as f:
        return yaml.safe_load(f)

# ── 类型映射 YAML → Prisma ──────────────────────────────────
def prisma_type(yaml_type: str) -> str:
    mapping = {
        "string":  "String",
        "number":  "Float",
        "integer": "Int",
        "int":     "Int",
        "int64":   "Int",
        "boolean": "Boolean",
        "bool":    "Boolean",
        "datetime":"DateTime",
        "json":    "Json",
    }
    return mapping.get(yaml_type, "String")

# ── 生成字段级别的 Prisma 属性 ──────────────────────────────
def field_attrs(field: dict, model_name: str) -> str:
    parts = []

    if field.get("pk"):
        parts.append("@id")

    default = field.get("default")
    if default is not None:
        if isinstance(default, bool):
            parts.append(f"@default({str(default).lower()})")
        elif isinstance(default, (int, float)):
            parts.append(f"@default({default})")
        elif isinstance(default, str) and "(" in default:
            parts.append(f"@default({default})")
        else:
            parts.append(f'@default("{default}")')

    fk = field.get("fk")
    if fk:
        on_delete = field.get("on_delete", "Cascade")
        parts.append(f'@relation("{model_name}To{fk}", fields: [{field["name"]}], references: [id], onDelete: {on_delete})')

    if field.get("map"):
        parts.append(f'@map("{field["map"]}")')

    if field.get("updated_at") or field["name"] == "updated_at":
        parts.append("@updatedAt")

    return " ".join(parts)

# ── 生成单个模型 ─────────────────────────────────────────────
def gen_model(entity: dict) -> str:
    name = entity["name"]
    description = entity.get("description", "")
    lines = []

    if description:
        lines.append(f"/// {description}")
    lines.append(f"model {name} {{")

    for field in entity.get("fields", []):
        fname = field["name"]
        ftype = prisma_type(field["type"])

        # Optional: fields are optional by default unless pk or required=true
        optional = ""
        if not field.get("pk") and not field.get("required", False):
            optional = "?"

        comment = field.get("description", "")
        attrs   = field_attrs(field, name)

        # Build the field line
        field_line = f"  {fname}  {ftype}{optional}"
        if attrs:
            field_line += f"  {attrs}"

        if comment:
            lines.append(f"  // {comment}")
        lines.append(field_line)

    lines.append("}")
    return "\n".join(lines)

# ── 生成 Prisma schema 头部 ─────────────────────────────────
def gen_header() -> str:
    return f"""// Code generated by spec-to-prisma v0.1 — {TODAY}
// DO NOT EDIT. Regenerate with: python3 generators/spec-to-prisma/gen.py

generator client {{
  provider = "prisma-client-js"
}}

datasource db {{
  provider = "postgresql"
  url      = env("DATABASE_URL")
}}
"""

# ── main ────────────────────────────────────────────────────
def main():
    # Resolve spec_root: find the directory containing 'feature' subdirectory
    spec_root = SPEC_DIR
    while spec_root.name != "feature" and spec_root.parent != spec_root:
        candidate = spec_root / "feature"
        if candidate.is_dir():
            break
        spec_root = spec_root.parent

    print(f"spec-to-prisma v0.1")
    print(f"  spec_dir  : {spec_root}")
    print(f"  output_dir: {OUT_DIR}")

    features = sorted([d.name for d in (spec_root / "feature").iterdir() if d.is_dir()])
    all_models = []

    for feature in features:
        feature_path = spec_root / "feature" / feature
        data_yaml = feature_path / f"{feature}_data.yaml"

        print(f"\n[{feature}]")
        if data_yaml.exists():
            data = load_yaml(data_yaml)
            entities = data.get("content", {}).get("entities", [])
            all_models.extend(entities)
            print(f"  ✓ loaded {data_yaml} ({len(entities)} entities)")
        else:
            print(f"  (no data.yaml, skipping)")

    OUT_DIR.mkdir(parents=True, exist_ok=True)
    schema_path = OUT_DIR / "schema.prisma"

    model_blocks = [gen_model(e) for e in all_models]
    content = gen_header() + "\n\n".join(model_blocks) + "\n"
    schema_path.write_text(content, encoding="utf-8")

    print(f"\n  → {schema_path}")
    print(f"\n✅ Done — {len(all_models)} models written to {schema_path}")

if __name__ == "__main__":
    main()
